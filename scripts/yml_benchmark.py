#!/usr/bin/env python
"""
YAML-driven physics-discovery benchmark.

Generates `results/yml_bench/<name>/run.sh`, executes it, then writes
`summary.txt` with mean ± std of explanation_score and mean_pos_error
per (model, world).

Usage:
    python scripts/yml_benchmark.py configs/my_config.yml
    python scripts/yml_benchmark.py configs/my_config.yml --no-run
    python scripts/yml_benchmark.py --aggregate-only results/yml_bench/my_run
"""

from __future__ import annotations

import argparse
import datetime
import json
import math
import shutil
import subprocess
import sys
from pathlib import Path

import numpy as np

_BOOTSTRAP_RESAMPLES = 5000
_BOOTSTRAP_SEED = 0  # fixed so summary.txt is reproducible across re-aggregation

try:
    import yaml
except ImportError:
    sys.stderr.write("error: PyYAML is not installed. Run `pip install pyyaml`.\n")
    sys.exit(2)


REPO_ROOT = Path(__file__).resolve().parent.parent


def load_config(path: Path) -> dict:
    with open(path) as f:
        cfg = yaml.safe_load(f)
    if not isinstance(cfg, dict):
        raise ValueError(f"{path}: top-level YAML must be a mapping")

    for k in ("models", "critic", "max_rounds", "worlds", "seeds"):
        if k not in cfg:
            raise ValueError(f"{path}: missing required field `{k}`")

    critic = cfg["critic"]
    if isinstance(critic, bool):
        critic = "on" if critic else "off"
    elif isinstance(critic, str):
        critic = critic.lower()
    if critic not in ("on", "off"):
        raise ValueError("critic must be 'on'/'off' (or true/false)")
    cfg["critic"] = critic
    if not isinstance(cfg["models"], list) or not cfg["models"]:
        raise ValueError("models must be a non-empty list")
    if not isinstance(cfg["worlds"], list) or not cfg["worlds"]:
        raise ValueError("worlds must be a non-empty list")
    if not isinstance(cfg["seeds"], list) or not cfg["seeds"]:
        raise ValueError("seeds must be a non-empty list")
    if not isinstance(cfg["max_rounds"], int):
        raise ValueError("max_rounds must be an int")

    cfg.setdefault("noise_std", 0.0)
    if not isinstance(cfg["noise_std"], (int, float)):
        raise ValueError("noise_std must be a number")
    cfg["noise_std"] = float(cfg["noise_std"])

    cfg.setdefault("critic_model", "claude-haiku-4-5-20251001")
    cfg.setdefault("name", datetime.datetime.now().strftime("%Y%m%d-%H%M%S"))
    return cfg


def generate_bash_script(cfg: dict, out_root: Path) -> Path:
    """Write run.sh inside out_root and return its path."""
    models_str = " ".join(f'"{m}"' for m in cfg["models"])
    worlds_str = " ".join(f'"{w}"' for w in cfg["worlds"])
    seeds_str = " ".join(str(s) for s in cfg["seeds"])

    script = f"""#!/usr/bin/env bash
# Auto-generated by scripts/yml_benchmark.py for benchmark "{cfg['name']}".
# Do not edit by hand — regenerate from configs/<...>.yml.
set -uo pipefail

REPO_ROOT="{REPO_ROOT}"
cd "${{REPO_ROOT}}"

if [[ -f "${{REPO_ROOT}}/.env" ]]; then
  set -a
  # shellcheck disable=SC1091
  source "${{REPO_ROOT}}/.env"
  set +a
else
  echo "WARNING: ${{REPO_ROOT}}/.env not found — API-key-backed calls will fail." >&2
fi

OUT_ROOT="{out_root}"

MODELS=( {models_str} )
WORLDS=( {worlds_str} )
SEEDS=( {seeds_str} )
CRITIC="{cfg['critic']}"
CRITIC_MODEL="{cfg['critic_model']}"
MAX_ROUNDS={cfg['max_rounds']}
NOISE_STD={cfg['noise_std']}

N_TOTAL=$(( ${{#MODELS[@]}} * ${{#WORLDS[@]}} * ${{#SEEDS[@]}} ))

echo "=========================================="
echo "YAML benchmark: {cfg['name']}"
echo "  output : ${{OUT_ROOT}}"
echo "  models : ${{MODELS[*]}}"
echo "  worlds : ${{WORLDS[*]}}"
echo "  seeds  : ${{SEEDS[*]}}"
echo "  critic : ${{CRITIC}} (model: ${{CRITIC_MODEL}})"
echo "  rounds : ${{MAX_ROUNDS}}"
echo "  noise  : σ=${{NOISE_STD}}"
echo "  total trials : ${{N_TOTAL}}"
echo "=========================================="

trial=0
t_start=$(date +%s)

for model in "${{MODELS[@]}}"; do
  safe_model="${{model//\\//_}}"
  for world in "${{WORLDS[@]}}"; do
    for seed in "${{SEEDS[@]}}"; do
      trial=$((trial + 1))
      base="${{OUT_ROOT}}/${{safe_model}}/${{world}}_seed${{seed}}"
      mkdir -p "$(dirname "${{base}}")"

      critic_flags=()
      if [[ "${{CRITIC}}" == "on" ]]; then
        critic_flags=(--use-critic --critic-model "${{CRITIC_MODEL}}")
      fi

      t_trial_start=$(date +%s)
      printf "[%3d/%3d] model=%s world=%s seed=%s\\n" \\
        "${{trial}}" "${{N_TOTAL}}" "${{model}}" "${{world}}" "${{seed}}"

      python ScienceAgent/run_discovery.py \\
        --model "${{model}}" \\
        --world "${{world}}" \\
        --noise-std "${{NOISE_STD}}" \\
        --noise-seed "${{seed}}" \\
        --max-rounds "${{MAX_ROUNDS}}" \\
        --store_output "${{base}}" \\
        --quiet \\
        ${{critic_flags[@]+"${{critic_flags[@]}}"}} \\
        > "${{base}}.stdout.log" 2>&1
      rc=$?
      t_trial_end=$(date +%s)

      if [[ ${{rc}} -ne 0 ]]; then
        echo "    FAILED (rc=${{rc}}) — see ${{base}}.stdout.log"
      else
        printf "    done in %ds\\n" "$((t_trial_end - t_trial_start))"
      fi
    done
  done
done

t_end=$(date +%s)
echo "=========================================="
echo "Finished ${{trial}} trials in $((t_end - t_start))s"
echo "=========================================="
"""
    script_path = out_root / "run.sh"
    script_path.write_text(script)
    script_path.chmod(0o755)
    return script_path


def aggregate(out_root: Path) -> Path:
    """Walk out_root for per-trial JSONs and write summary.txt."""
    by_trial: dict[tuple[str, str], list[tuple[float | None, float | None]]] = {}

    for json_path in sorted(out_root.rglob("*.json")):
        if json_path.name == "config.json":
            continue
        try:
            with open(json_path) as f:
                d = json.load(f)
        except (OSError, json.JSONDecodeError):
            continue
        model = d.get("model")
        world = d.get("world")
        ev = d.get("evaluation") or {}
        if not model or not world or not ev:
            continue
        mpe = ev.get("mean_pos_error")
        expl = ev.get("explanation") or {}
        score = expl.get("score") if isinstance(expl, dict) else None
        by_trial.setdefault((model, world), []).append((mpe, score))

    lines = []
    lines.append(f"Benchmark summary  ({out_root.name})")
    lines.append("=" * 116)
    header = (
        f"{'model':<50} {'world':<15} {'n':>3} "
        f"{'expl_score [95% CI]':>22} {'mean_pos_err [95% CI]':>22}"
    )
    lines.append(header)
    lines.append("-" * 116)

    for (model, world), values in sorted(by_trial.items()):
        scores = [
            float(s)
            for _, s in values
            if isinstance(s, (int, float)) and math.isfinite(s)
        ]
        errs = [
            float(e)
            for e, _ in values
            if isinstance(e, (int, float)) and math.isfinite(e)
        ]
        n = len(values)
        score_str = _fmt_mean_bootstrap(scores)
        err_str = _fmt_mean_bootstrap(errs)
        lines.append(f"{model:<50} {world:<15} {n:>3} {score_str:>22} {err_str:>22}")

    lines.append("-" * 116)
    lines.append("expl_score: explanation judge score in [0, 1], higher is better.")
    lines.append(
        "mean_pos_err: trajectory mean position error, lower is better; pass < 0.1."
    )
    lines.append(
        f"Format: mean [2.5%, 97.5%] from {_BOOTSTRAP_RESAMPLES} bootstrap resamples "
        f"of the mean (seed={_BOOTSTRAP_SEED}). n=1 → no CI. 'n/a' = no successful runs."
    )

    out_path = out_root / "summary.txt"
    out_path.write_text("\n".join(lines) + "\n")
    _make_plots(by_trial, out_root)
    _write_per_model_summary(by_trial, out_root)
    return out_path


def _per_model_pooled(by_trial):
    """Pool every trial equally; return {model: (errs, scores)} lists."""
    pooled: dict[str, tuple[list[float], list[float]]] = {}
    for (model, _world), values in by_trial.items():
        errs, scores = pooled.setdefault(model, ([], []))
        for e, s in values:
            if isinstance(e, (int, float)) and math.isfinite(e):
                errs.append(float(e))
            if isinstance(s, (int, float)) and math.isfinite(s):
                scores.append(float(s))
    return pooled


def _write_per_model_summary(by_trial, out_root: Path) -> Path:
    pooled = _per_model_pooled(by_trial)
    lines = []
    lines.append(f"Per-model summary  ({out_root.name})")
    lines.append("=" * 116)
    header = (
        f"{'model':<50} {'n_trials':>9} "
        f"{'expl_score [95% CI]':>22} {'mean_pos_err [95% CI]':>22}"
    )
    lines.append(header)
    lines.append("-" * 116)
    for model in sorted(pooled):
        errs, scores = pooled[model]
        n = max(len(errs), len(scores))
        lines.append(
            f"{model:<50} {n:>9} "
            f"{_fmt_mean_bootstrap(scores):>22} {_fmt_mean_bootstrap(errs):>22}"
        )
    lines.append("-" * 116)
    lines.append("Pooled across all worlds and seeds (every trial counts equally).")
    lines.append(
        f"Format: mean [2.5%, 97.5%] from {_BOOTSTRAP_RESAMPLES} bootstrap resamples "
        f"of the mean (seed={_BOOTSTRAP_SEED}). n=1 → no CI. 'n/a' = no successful runs."
    )
    out_path = out_root / "summary_per_model.txt"
    out_path.write_text("\n".join(lines) + "\n")
    return out_path


def _short(model: str) -> str:
    """Trim long provider-prefixed model names for legend display."""
    return model.rsplit("/", 1)[-1]


def _trial_values(by_trial, model, world):
    values = by_trial.get((model, world), [])
    errs = [
        float(e) for e, _ in values if isinstance(e, (int, float)) and math.isfinite(e)
    ]
    scores = [
        float(s) for _, s in values if isinstance(s, (int, float)) and math.isfinite(s)
    ]
    return errs, scores


def _bootstrap_ci(values):
    """Return (mean, err_lo, err_hi) — distances to the 2.5/97.5 percentiles."""
    if not values:
        return None
    if len(values) == 1:
        return (values[0], 0.0, 0.0)
    arr = np.asarray(values, dtype=float)
    rng = np.random.default_rng(_BOOTSTRAP_SEED)
    idx = rng.integers(0, arr.size, size=(_BOOTSTRAP_RESAMPLES, arr.size))
    boot = arr[idx].mean(axis=1)
    lo, hi = np.percentile(boot, [2.5, 97.5])
    m = float(arr.mean())
    return (m, m - lo, hi - m)


def _make_plots(by_trial, out_root: Path) -> None:
    """Write summary.{png,pdf} (grouped bars) and runs.{png,pdf} (strip plot)."""
    if not by_trial:
        return
    try:
        import matplotlib

        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        print("matplotlib not available; skipping plots", file=sys.stderr)
        return

    models = sorted({m for m, _ in by_trial.keys()})
    worlds = sorted({w for _, w in by_trial.keys()})
    _make_bar_plot(by_trial, models, worlds, out_root, plt)
    _make_strip_plot(by_trial, models, worlds, out_root, plt)
    _make_per_model_plot(by_trial, models, out_root, plt)


def _make_per_model_plot(by_trial, models, out_root, plt) -> None:
    """One bar per model in two side-by-side panels: expl. score and MSE."""
    pooled = _per_model_pooled(by_trial)
    n_models = len(models)
    if n_models == 0:
        return
    fig_w = max(8.0, 1.2 * n_models * 2)
    fig, (ax_score, ax_err) = plt.subplots(1, 2, figsize=(fig_w, 5))
    x = np.arange(n_models)
    cmap = plt.get_cmap("tab10")
    colors = [cmap(i % 10) for i in range(n_models)]

    score_means, score_lo, score_hi = [], [], []
    err_means, err_lo, err_hi = [], [], []
    for model in models:
        errs, scores = pooled.get(model, ([], []))
        s = _bootstrap_ci(scores)
        e = _bootstrap_ci(errs)
        score_means.append(s[0] if s else np.nan)
        score_lo.append(s[1] if s else 0.0)
        score_hi.append(s[2] if s else 0.0)
        err_means.append(e[0] if e else np.nan)
        err_lo.append(e[1] if e else 0.0)
        err_hi.append(e[2] if e else 0.0)

    ax_score.bar(x, score_means, yerr=[score_lo, score_hi], color=colors, capsize=3)
    ax_err.bar(x, err_means, yerr=[err_lo, err_hi], color=colors, capsize=3)

    labels = [_short(m) for m in models]
    for ax, title, ylabel in [
        (
            ax_score,
            "Explanation score (pooled across worlds)",
            "score [0, 1]",
        ),
        (
            ax_err,
            "Mean position error (pooled across worlds)",
            "error",
        ),
    ]:
        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=20, ha="right")
        ax.set_ylabel(ylabel)
        ax.set_title(title)
        ax.grid(axis="y", linestyle=":", alpha=0.4)
    ax_score.set_ylim(0, 1.05)
    ax_err.set_yscale("log")

    fig.suptitle(f"Per-model rollup: {out_root.name}", y=1.02)
    fig.tight_layout()
    fig.savefig(out_root / "summary_per_model.png", dpi=150, bbox_inches="tight")
    fig.savefig(out_root / "summary_per_model.pdf", bbox_inches="tight")
    plt.close(fig)


def _make_bar_plot(by_trial, models, worlds, out_root, plt) -> None:
    n_models = len(models)
    n_worlds = len(worlds)
    # Stacked layout: explanation score on top, position error on bottom,
    # sharing one set of world tick labels across both panels.
    fig_w = max(8.0, 0.8 * n_worlds * max(1, n_models))
    fig, (ax_score, ax_err) = plt.subplots(2, 1, figsize=(fig_w, 9), sharex=True)
    width = 0.8 / max(1, n_models)
    x = np.arange(n_worlds)
    cmap = plt.get_cmap("tab10")

    for i, model in enumerate(models):
        sh, sl, su = [], [], []
        eh, el, eu = [], [], []
        for w in worlds:
            errs, scores = _trial_values(by_trial, model, w)
            s = _bootstrap_ci(scores)
            e = _bootstrap_ci(errs)
            sh.append(s[0] if s else np.nan)
            sl.append(s[1] if s else 0.0)
            su.append(s[2] if s else 0.0)
            eh.append(e[0] if e else np.nan)
            el.append(e[1] if e else 0.0)
            eu.append(e[2] if e else 0.0)
        offset = (i - (n_models - 1) / 2) * width
        color = cmap(i % 10)
        ax_score.bar(
            x + offset,
            sh,
            width=width,
            yerr=[sl, su],
            label=_short(model),
            color=color,
            capsize=3,
        )
        ax_err.bar(
            x + offset,
            eh,
            width=width,
            yerr=[el, eu],
            color=color,
            capsize=3,
        )

    for ax, title, ylabel in [
        (ax_score, "Explanation score (higher = better)", "score [0, 1]"),
        (ax_err, "Mean position error (lower = better)", "error"),
    ]:
        ax.set_xticks(x)
        ax.set_ylabel(ylabel)
        ax.set_title(title)
        ax.grid(axis="y", linestyle=":", alpha=0.4)
    # With sharex, only the bottom panel needs the world labels.
    ax_err.set_xticklabels(worlds, rotation=20, ha="right")
    ax_score.set_ylim(0, 1.05)
    ax_err.set_yscale("log")

    fig.suptitle(f"Benchmark: {out_root.name}", y=1.00)
    handles, labels = ax_score.get_legend_handles_labels()
    fig.legend(
        handles,
        labels,
        loc="lower center",
        ncol=min(4, max(1, n_models)),
        bbox_to_anchor=(0.5, -0.02),
    )
    fig.tight_layout()
    fig.savefig(out_root / "summary.png", dpi=150, bbox_inches="tight")
    fig.savefig(out_root / "summary.pdf", bbox_inches="tight")
    plt.close(fig)


def _make_strip_plot(by_trial, models, worlds, out_root, plt) -> None:
    n_models = len(models)
    n_worlds = len(worlds)
    fig_w = max(8.0, 1.3 * n_worlds * max(1, n_models))
    fig, (ax_score, ax_err) = plt.subplots(1, 2, figsize=(fig_w, 5))
    spread = 0.7 / max(1, n_models)
    x = np.arange(n_worlds)
    cmap = plt.get_cmap("tab10")
    rng = np.random.default_rng(_BOOTSTRAP_SEED)

    for i, model in enumerate(models):
        offset = (i - (n_models - 1) / 2) * spread
        color = cmap(i % 10)
        first_label_used = False
        for j, w in enumerate(worlds):
            errs, scores = _trial_values(by_trial, model, w)
            if scores:
                jitter = rng.normal(0, 0.03, size=len(scores))
                ax_score.scatter(
                    np.full(len(scores), x[j] + offset) + jitter,
                    scores,
                    color=color,
                    alpha=0.75,
                    s=30,
                    edgecolors="white",
                    linewidth=0.5,
                    label=_short(model) if not first_label_used else None,
                )
                first_label_used = True
            if errs:
                jitter = rng.normal(0, 0.03, size=len(errs))
                ax_err.scatter(
                    np.full(len(errs), x[j] + offset) + jitter,
                    errs,
                    color=color,
                    alpha=0.75,
                    s=30,
                    edgecolors="white",
                    linewidth=0.5,
                )

    for ax, title, ylabel in [
        (ax_score, "Explanation score per run", "score [0, 1]"),
        (ax_err, "Mean position error per run", "error"),
    ]:
        ax.set_xticks(x)
        ax.set_xticklabels(worlds, rotation=20, ha="right")
        ax.set_ylabel(ylabel)
        ax.set_title(title)
        ax.grid(axis="y", linestyle=":", alpha=0.4)
    ax_score.set_ylim(-0.05, 1.05)
    ax_err.set_yscale("log")

    fig.suptitle(f"Benchmark (per-run): {out_root.name}", y=1.02)
    handles, labels = ax_score.get_legend_handles_labels()
    if handles:
        fig.legend(
            handles,
            labels,
            loc="lower center",
            ncol=min(4, max(1, n_models)),
            bbox_to_anchor=(0.5, -0.05),
        )
    fig.tight_layout()
    fig.savefig(out_root / "runs.png", dpi=150, bbox_inches="tight")
    fig.savefig(out_root / "runs.pdf", bbox_inches="tight")
    plt.close(fig)


def _fmt_mean_bootstrap(values: list[float]) -> str:
    """Mean and 95% bootstrap CI of the mean (`_BOOTSTRAP_RESAMPLES` resamples)."""
    if not values:
        return "n/a"
    if len(values) == 1:
        return f"{values[0]:.3f}"
    arr = np.asarray(values, dtype=float)
    rng = np.random.default_rng(_BOOTSTRAP_SEED)
    idx = rng.integers(0, arr.size, size=(_BOOTSTRAP_RESAMPLES, arr.size))
    boot_means = arr[idx].mean(axis=1)
    lo, hi = np.percentile(boot_means, [2.5, 97.5])
    return f"{float(arr.mean()):.3f} [{lo:.3f}, {hi:.3f}]"


def main() -> int:
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("config", nargs="?", help="Path to YAML config")
    parser.add_argument(
        "--no-run",
        action="store_true",
        help="Generate run.sh but do not execute it",
    )
    parser.add_argument(
        "--aggregate-only",
        metavar="DIR",
        help="Skip generation/run; aggregate an existing output dir",
    )
    parser.add_argument(
        "--out-root",
        default=None,
        help="Override output root (default: results/yml_bench/<name>)",
    )
    args = parser.parse_args()

    if args.aggregate_only:
        out_root = Path(args.aggregate_only)
        if not out_root.is_dir():
            print(f"error: {out_root} is not a directory", file=sys.stderr)
            return 1
        path = aggregate(out_root)
        print(f"summary written to {path}")
        print()
        print(path.read_text())
        return 0

    if not args.config:
        parser.error("config is required (unless --aggregate-only)")

    cfg = load_config(Path(args.config))
    out_root = (
        Path(args.out_root)
        if args.out_root
        else REPO_ROOT / "results" / "yml_bench" / cfg["name"]
    )
    out_root.mkdir(parents=True, exist_ok=True)

    shutil.copy(args.config, out_root / "config.yml")
    script_path = generate_bash_script(cfg, out_root)
    print(f"Wrote {script_path}")

    if args.no_run:
        print("(--no-run requested; skipping execution)")
        return 0

    print(f"Executing {script_path} ...\n")
    rc = subprocess.run(["bash", str(script_path)]).returncode
    if rc != 0:
        print(f"\nrun.sh exited with {rc}; partial results may exist", file=sys.stderr)

    summary_path = aggregate(out_root)
    print(f"\nsummary written to {summary_path}")
    print(f"--- {summary_path.name} ---")
    print(summary_path.read_text())
    return rc


if __name__ == "__main__":
    sys.exit(main())
